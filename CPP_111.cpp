map<char, int> histogram(string test) {
    map<char, int> result;
    if (test.empty()) return result;

    string letters = test;
    for (char c : unique(letters.begin(), letters.end())) {
        int count = 0;
        for (char letter : letters) {
            if (letter == c) {
                count++;
            }
        }
        if (count > 0) {
            result[c] = count;
        }
    }

    return result;
}