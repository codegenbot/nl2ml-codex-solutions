def match_parens(lst):
    if len(lst) == 1:
        if lst[0].count("(") == lst[0].count(")"):
            return "Yes"
        else:
            return "No"
    else:
        if lst[0].count("(") == lst[0].count(")"):
            return match_parens([lst[0] + lst[1]])
        else:
            return match_parens([lst[0] + lst[1]])







































































































































































































































































































































































































































































































































































































































































































































































































































































