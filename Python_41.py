def car_race_collision(n: int):
    """
    Imagine a road that's a perfectly straight infinitely long line.
    n cars are driving left to right;  simultaneously, a different set of n cars
    are driving right to left.   The two sets of cars start out being very far from
    each other.  All cars move in the same speed.  Two cars are said to collide
    when a car that's moving left to right hits a car that's moving right to left.
    However, the cars are infinitely sturdy and strong; as a result, they continue moving
    in their trajectory as if they did not collide.

    This function outputs the number of such collisions.
    """

    #  the following is the solution to this problem.
    #  the problem is to find the number of pairs of numbers in two lists that
    #  are equal.
    #  the following solution is O(n^2) in time and O(1) in space.
    #  the challenge is to come up with an algorithm that is
    #  O(nlogn) in time and O(1) in space.

    collisions = 0
    for i in range(n):
        for j in range(n):
            if i == j:
                collisions += 1

    return collisions


