#include <iostream>
#include <vector>

using namespace std;

float max_element(vector<float> l){
    float max = l[0];
    for (int i=1; i<l.size(); i++) {
        if (l[i] > max) {
            max = l[i];
        }
    }
    return max;
}

int main() {
    assert(abs(max_element({5.0f, 3.0f, -5.0f, 2.0f, -3.0f, 3.0f, 9.0f, 0.0f, 124.0f, 1.0f, -10.0f})- 124)<1e-4);
    return 0;
}