def fix_spaces(text):
    return (
        text.replace(" ", "_")
        .replace("  ", "_-")
        .replace("_  ", "_-_")
        .replace("_   ", "_-_")
        .replace("_  ", "-_")
        .replace("_   ", "-_")
        .replace("_    ", "-----")
        .replace("_   ", "-")
        .replace("_  ", "-_")
        .replace("_    ", "_---")
        .replace("_   ", "-_")
        .replace("_  ", "-_")
        .replace("   ", "--")
        .replace("  ", "-")
        .replace("    ", "----")
        .replace("   ", "--")
        .replace("  ", "-")
        .replace("    ", "_---")
        .replace("   ", "-_")
        .replace("  ", "-_")
        .replace("___", "-----")
    )