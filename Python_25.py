from typing import List

def factorize(n: int) -> List[int]:
    factors = []
    divisor = 2
    while n > 1:
        if n % divisor == 0:
            factors.append(divisor)
            n //= divisor
        else:
            divisor += 1
    return factors

numbers = list(map(int, input().split()))
for num in numbers:
    factors = factorize(num)
    print(*factors)