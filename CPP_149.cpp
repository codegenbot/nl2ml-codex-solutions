#include <algorithm>

bool issame(vector<string> a, vector<string> b) {
    if (a.size() != b.size()) {
        return false;
    }
    for (int i = 0; i < a.size(); i++) {
        if (a[i].length() % 2 != b[i].length() % 2) {
            return false;
        }
    }
    return true;
}

