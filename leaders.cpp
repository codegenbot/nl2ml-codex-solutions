string add(string str1, string str2){
    int len, len1 = str1.size(), len2 = str2.size();
    string str;
    if(len1 >= len2){
        len = len1;
        str2 = string(len - len2, '0') + str2;
    }else if(len1 < len2){
        len = len2;
        str1 = string(len - len1, '0') + str1;
    }
    str = string(len - 1, '0');
    reverse(str1.begin(), str1.end());
    reverse(str2.begin(), str2.end());
    int carry = 0;
    char ch[2];
    for(int i = 0; i < len; i++){
        carry += str1[i] - '0' + str2[i] - '0';
        ch[0] = carry % 10 + '0';
        carry /= 10;
        str += ch;
        ch[0] = '\0';
    }
    if(carry)
        str += '1';
    reverse(str.begin(), str.end());
    return str;
}
