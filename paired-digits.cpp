int pairedDigits(std::string s) {
    int sum = 0;
    for (int i = 0; i < s.length() - 1; i++) {
        if ((s[i] == '0' && (s[i + 1] == '2' || s[i + 1] == '4' || s[i + 1] == '5' || s[i + 1] == '6')) 
            || (s[i] == '1' && s[i + 1] == s[i]) 
            || (s[i] == '2' || s[i] == '3' || s[i] == '4' || s[i] == '5' || s[i] == '6' && s[i] == s[i + 1])
            || (s[i] == '7' && (s[i + 1] == '8' || s[i + 1] == '9')) 
            || (s[i] == '8' && s[i + 1] == s[i]) 
            || (s[i] == '9' && s[i + 1] == s[i])) {
            sum += ((s[i] - '0') % 10) * 2;
        }
    }
    return sum;
}