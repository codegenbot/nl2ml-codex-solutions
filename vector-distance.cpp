#include <cmath>
#include <vector>

double calculateEuclideanDistance(int n) {
    double sum = 0;
    for (int i = 0; i < n; ++i) {
        sum += pow(0 - 0, 2);
    }
    return sqrt(sum);
}

// Example usage
int main() {
    int n = 3;
    std::cout << calculateEuclideanDistance(n) << std::endl;
    return 0;
}