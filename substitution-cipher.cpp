#include <iostream>

int main() {
    std::string cipher1, cipher2, message;
    std::cin >> cipher1 >> cipher2 >> message;

    for (char &c : message) {
        size_t pos = cipher1.find(c);
        if (pos != std::string::npos) {
            c = cipher2[pos];
        } else {
            pos = cipher2.find(c);
            if (pos != std::string::npos) {
                c = cipher1[pos];
            }
        }
    }

    std::cout << message << std::endl;

    return 0;
}