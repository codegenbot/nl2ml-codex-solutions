#include <vector>
using namespace std;

int specialFilter(vector<int> nums) {
    int count = 0;
    for (int num : nums) {
        if (abs(num) > 10 && (num % 10) % 2 != 0 && (num / 10) % 10 % 2 != 0) {
            count++;
        }
    }
    return count;
}

int main() {
    vector<int> numbers = {1001, -2003, 3004, -4005};
    cout << "The number of special numbers is: " << specialFilter(numbers) << endl;
    return 0;
}